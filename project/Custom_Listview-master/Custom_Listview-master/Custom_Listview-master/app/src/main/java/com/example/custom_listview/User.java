package com.example.custom_listview;

public class User {

    String name, location, speed, phoneNo, country;
    int imageId;

    public User(String name, String location, String speed, String phoneNo, String country, int imageId) {
        this.name = name;
        this.location = location;
        this.speed = speed;
        this.phoneNo = phoneNo;
        this.country = country;
        this.imageId = imageId;
    }
}
